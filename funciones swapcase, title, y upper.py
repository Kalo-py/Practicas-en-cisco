# El método swapcase() crea una nueva cadena intercambiando 
# todas las letras por mayúsculas o minúsculas dentro de la cadena original: los caracteres en mayúscula se convierten en minúsculas y viceversa.
print("Yo sé que no sé nada.".swapcase())

print()

#El método title() realiza una función algo similar cambia la primera letra de cada palabra a mayúsculas, convirtiendo todas las demás a minúsculas.
print("Yo sé que no sé nada. Parte 1.".title())

print()

#Por último, pero no menos importante, el método upper() 
# hace una copia de la cadena de origen, reemplaza todas las letras minúsculas con sus equivalentes en mayúsculas, y devuelve la cadena como resultado.
print("Yo sé que no sé nada. Parte 2.".upper())